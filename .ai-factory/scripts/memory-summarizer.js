import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const memoryManagerPath = path.resolve(__dirname, '../../scripts/memory-manager.js');

let MemoryManager = null;
try {
    const mm = await import(memoryManagerPath);
    MemoryManager = mm.MemoryManager || mm.default;
} catch (e) {
    console.error('Falha ao carregar MemoryManager:', e);
}

class MemorySummarizer {
    constructor() {
        this.mm = MemoryManager ? new MemoryManager() : null;
    }

    async summarize() {
        if (!this.mm) return;
        
        console.log('🧠 Gerando resumo de memórias recentes...');
        
        // Simula a busca de memórias recentes (últimas 24h ou últimos 20 eventos)
        // O ideal é buscar do sqlite-vec os eventos com tag 'watcher' ou 'file_event'
        let recentEvents = [];
        try {
            if (typeof this.mm.searchMemories === 'function') {
                // Isso é um placeholder, na realidade seria necessário um fetch baseado em timestamp
                recentEvents = ["Evento simulado: mudança em App.tsx", "Evento simulado: git commit feito"]; 
            }
            
            // Simulação de chamada de IA para resumir
            const prompt = `Resuma os seguintes eventos de forma concisa em português:\n${recentEvents.join('\n')}`;
            const model = process.env.AGENT_MODEL || 'gemini-2.5-pro';
            
            // Simula a resposta da IA
            const summary = `Resumo Automático (${model}): O usuário realizou alterações em arquivos do projeto e executou comandos relacionados a versionamento.`;
            
            // Salva o resumo
            if (typeof this.mm.saveMemory === 'function') {
                this.mm.saveMemory(summary, { agent: 'summarizer', type: 'summary', autoGenerated: true });
                console.log('✅ Resumo salvo na memória persistente.');
            }
        } catch (e) {
            console.error('Erro na sumarização:', e);
        }
    }
}

export default new MemorySummarizer();
